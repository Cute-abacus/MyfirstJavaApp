package entity;

public class User {

	private String user_id;
	private String user_name;
	private String user_sex;
	private String user_question;
	private String user_answer;
	private String user_number;
	private String user_nick;
	private String user_code;
	private int user_right;
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String i) {
		this.user_id = i;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getUser_sex() {
		return user_sex;
	}
	public void setUser_sex(String user_sex) {
		this.user_sex = user_sex;
	}
	public String getUser_question() {
		return user_question;
	}
	public void setUser_question(String user_question) {
		this.user_question = user_question;
	}
	public String getUser_answer() {
		return user_answer;
	}
	public void setUser_answer(String user_answer) {
		this.user_answer = user_answer;
	}
	public String getUser_number() {
		return user_number;
	}
	public void setUser_number(String user_number) {
		this.user_number = user_number;
	}
	public String getUser_nick() {
		return user_nick;
	}
	public void setUser_nick(String user_nick) {
		this.user_nick = user_nick;
	}
	public String getUser_code() {
		return user_code;
	}
	public void setUser_code(String user_code) {
		this.user_code = user_code;
	}
	public int getUser_right() {
		return user_right;
	}
	public void setUser_right(int user_right) {
		this.user_right = user_right;
	}	

}
